package aula01;


public class Operacoes {
    public static void main(String[] args){
        int a = 10;
        int b = 5;

        //adição;
        int soma = a + b;
        System.out.println(soma);

        float divisao = 10.0f / 3.0f;
        System.out.println(divisao);

        System.out.println("O resto de A / B = " + a%b + ", e o resto de 10 / 3 é " + 10.0f%3.0f);
    }
}
